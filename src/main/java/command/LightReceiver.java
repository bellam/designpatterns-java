package command;

public class LightReceiver {

    public void on() {
        System.out.println("\uD83D\uDC9B LIGHT ON");
    }

    public void off() {
        System.out.println("\uD83D\uDDA4 LIGHT OFF");
    }

    public void dimmer() {
        System.out.println("\uD83D\uDC9C LIGHT DIM");
    }

    public void upper() {
        System.out.println("❤️ LIGHT UPPER");
    }
}
